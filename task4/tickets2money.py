class tickets2money():
    # this class is used to convert the number of particular tickets sold, into revenue to be used for tables and plotting
    def adult(num):
        return num*5.0
    def student(num):
        return num*4.0
    def child(num):
        return num*4.0
    def OAP(num):
        return num*4.0
    def VIP(num):
        return num*6.0
